n = input("Digite qualquer coisa: \n ")

print ( " O tipo do valor é: ", type(n))


print ("O Valor é um tipo númerico: ", n.isnumeric())
print ("O Valor é um tipo alfabético: ", n.isalpha()) 
print ("O Valor é um tipo espaço: ", n.isspace())
print ("O Valor é um tipo alfaNúmero: ", n.isalnum())
print ("O Valor é um tipo maiúscula: ", n.isupper())
print ("O Valor é um tipo minuscula: ", n.islower())
print ("O Valor é um tipo é Capitalizada : ", n.istitle())
